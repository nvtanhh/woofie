@lazySingleton
class GetPostOfUserUsecase {
  final ProfileRepository _profileRepository;

  GetPostOfUserUsecase(this._profileRepository);

  Future<List<Post>> call(int useId, {int offset = 0, int limit = 10}) async {
    return _profileRepository.getPostOfUser(useId, offset, limit);
  }
}
